.entryp main;
.section data;
	string times " x ";
	string eq " = ";
	string nl "\n";
	string expected "Success: expected result\n";
	string unexpected "Failed: unexpected result\n";
.section code;
	.sub success;
		print @expected;
		halt;
	.ends;
	.sub failure;
		print @unexpected;
		halt;
	.ends;
	.sub main;
		mov $10 %g1;
		mov $0xA %g2;
		mul %g1 %g2;
		print %g1;
		print @times;
		print %g2;
		print @eq;
		print %m;
		print @nl;
		cmp %m $100;
		je !success;
		jmp !failure;
	.ends;

